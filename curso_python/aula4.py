# Tipod int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é 
# positivo.

print(11,'Esses são do tipo int' ,sep=' -> ')
print(-11, 'Esses são do tipo int' ,sep=' -> ')
print(0 ,'Esses são do tipo int' ,sep=' -> ')


#Float -> Número com ponto flutuante 
#O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante. 
# float sem sinal é considerado positivo 

print(1.1,0.1, 'Esses são do tipo float' ,sep=' -> ')

# A função type mostra o tipo que o Python 
# inferiu ao valor.

print(type('Felipe'))
print(type(1.1111111))
print(type(11.11),type('Jessica'),type(24))